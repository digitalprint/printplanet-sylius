declare(strict_types=1);

namespace PrintPlanet\Sylius\Component\Product\Model;

use PrintPlanet\Sylius\Component\Resource\Model\AbstractTranslation;

class ProductOptionValueTranslation extends AbstractTranslation implements ProductOptionValueTranslationInterface
{
    /** @var mixed */
    protected $id;

    /** @var string */
    protected $value;

    /** @var ProductOptionValueInterface */
    protected $optionValue;

    /**
     * {@inheritdoc}
     */
    public function getId()
    {
        return $this->id;
    }

    /**
     * {@inheritdoc}
     */
    public function getValue(): ?string
    {
        return $this->value;
    }

    /**
     * {@inheritdoc}
     */
    public function setValue(?string $value): void
    {
        $this->value = $value;
    }
}
